# ─────────────────────────────────────────────
# Генератор індикатора логічної матриці
# ─────────────────────────────────────────────
def generate_logframe(sector: str) -> Logframe:
    text = (sector or "").lower()

    if "енерг" in text:
        return Logframe(
            indicator="До [дата] енергоспоживання цільового об'єкту знижено на 50-60% (базове значення — поточний енергопаспорт)",
            source="Щомісячні показники лічильників + енергоаудит ПІСЛЯ",
        )
    if "вод" in text:
        return Logframe(
            indicator="До [дата] щонайменше [N] мешканців отримують безперебійне водопостачання (з поточного базового значення)",
            source="Щоквартальний звіт комунального підприємства + анкетування домогосподарств",
        )
    if "соціальн" in text:
        return Logframe(
            indicator="До [дата] індекс довіри між ВПО та приймаючою громадою зріс на [N]% (базове опитування + фінальне)",
            source="Опитування мешканців (місцеві + ВПО) на початку і наприкінці проєкту",
        )
    if "освіт" in text:
        return Logframe(
            indicator="До [дата] [N]% учнів мають доступ до оновленого освітнього середовища, успішність зросла на [N]%",
            source="Дані шкільного журналу + анкетування учнів та батьків",
        )
    return Logframe(
        indicator="До [дата] [кількість] мешканців Жовтанецької громади [конкретна зміна] (з поточних [базове значення])",
        source="[Конкретний документ] + [метод перевірки (опитування, звіт, акт)]",
    )


