    steps.append({"phase": "🔬 АНАЛІЗ", "text": "Вивчити досвід інших громад (DREAM, case studies)", "detail": "Знайдіть 2-3 приклади подібних проєктів: що спрацювало, що ні"})

    steps.append({"phase": "✍️ НАПИСАННЯ", "text": "Сформулювати проблему (НЕ «немає X», а «через відсутність X страждають Y людей»)", "detail": "Донор фінансує зміни, а не об'єкти. Problem statement — найважливіша частина"})
