    tips: list[str] = []
    d = (donor_hint or "").lower()

    if any(k in d for k in ("єс", "eu", "єк", "interreg", "єіб", "eib")):
        tips.append("🇪🇺 СПЕЦИФІКА ЄС:")
        tips.append("Гендерна рівність, інклюзивність — обов'язково")
        tips.append("Зв'яжіть з євроінтеграцією та acquis communautaire")
        if "interreg" in d:
            tips.append("Транскордонний вимір: польський партнер обов'язковий")
        if "єіб" in d or "eib" in d:
            tips.append("Точні розрахунки: кВт·год/рік, тонни CO2, термін окупності")
        tips.append("Логічна матриця (Logframe) обов'язкова")
    if "usaid" in d or "pfru" in d:
        tips.append("🇺🇸 СПЕЦИФІКА USAID/PFRU:")
        tips.append("Civic engagement, social cohesion, democratic governance")
        tips.append("Local ownership — громада веде, донор допомагає")
        tips.append("Sustainability plan — що буде після закінчення гранту")
        tips.append("Залучення вразливих груп: ВПО, ветерани, молодь, люди з інвалідністю")
    if "giz" in d or "німеч" in d:
        tips.append("🇩🇪 СПЕЦИФІКА GIZ:")
        tips.append("Capacity building, системний підхід")
        tips.append("Replicability — модель для інших громад")
        tips.append("Навчальний компонент обов'язковий")
    if "проон" in d or "undp" in d:
        tips.append("🇺🇳 СПЕЦИФІКА ПРООН:")
        tips.append("SDGs — конкретні Цілі сталого розвитку")
        tips.append("Human rights-based approach")
        tips.append("Leave no one behind — вразливі групи")
    if "sida" in d or "швец" in d or "швед" in d:
        tips.append("🇸🇪 СПЕЦИФІКА SIDA:")
        tips.append("Права людини та гендерна рівність — ядро заявки")
        tips.append("Participatory approach — залучення бенефіціарів")
        tips.append("Прозорість і антикорупційний компонент")
    if "єднання" in d or "isar" in d or "ednannia" in d:
        tips.append("🇺🇦 СПЕЦИФІКА ЄДНАННЯ:")
